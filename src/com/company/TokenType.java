package com.company;

public enum TokenType {
    SPACES,         // пробел
    ADD,            // символ "+"
    SUB,            // символ "-"
    MUL,            // символ "*"
    DIV,            // символ "/"
    EXCLAM,         // символ "!"
    SEMICOLON,      // символ ";"
    NUMBER,         // число
    WHILE,
    SIXTEEN,        // шестандцатиричное число
    DO,
    INCR,           // инкремент
    DECR,           // декремент
    LPAR,           // символ "("
    RPAR,           // символ ")"
    LFIGBR,         // символ "{"
    RFIGBR,         // символ "}"
    VAR,            // переменная
    EQUATE,         // приравнивание
    ASSIGN,         // присваивание
    MORE,           // больше
    LESS,           // меньше
    EQUAL,          // равно
    MORE_OR_EQUAL,  // больше или равно
    LESS_OR_EQUAL,  // меньше или равно
    NOT_EQUAl,       // не равно

}

